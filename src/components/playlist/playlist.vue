<template>
  <transition name="list-fade">
    <div class="playlist" v-show="showFlag" @click="hide">
      <div class="list-wrapper" @click.stop>
        <div class="list-header">
          <h1 class="title">
            <i class="icon" :class="iconMode" @click="changeMode"></i>
            <span class="text">{{modeText}}</span>
            <span class="clear" @click="showConfirm"><i class="icon-clear"></i></span>
          </h1>
        </div>
        <scroll ref="listContent" :data="sequenceList"  class="list-content" :refreshDelay="refreshDelay">
          <transition-group ref="list" name="list" tag="ul">
            <li ref="listItem" class="item" v-for="(item,index) in sequenceList" :key="item.id" @click="selectItem(item,index)">
              <i class="current" :class="getCurrentIcon(item)"></i>
              <span class="text">{{item.name}}</span>
              <span class="like">
                <i ></i>
              </span>
              <span class="delete" @click.stop="deleteOne(item)">
                <i class="icon-delete"></i>
              </span>
            </li>
          </transition-group>
        </scroll>
        <div class="list-operate">
          <div class="add" @click="addSong">
            <i class="icon-add"></i>
            <span class="text">添加歌曲到队列</span>
          </div>
        </div>
        <div class="list-close" @click="hide">
          <span>关闭</span>
        </div>
      </div>
      <confirm ref="confirm" @confirm="confirmClear" text="是否清空播放列表" confirmBtnText="清空" />
      <add-song ref="addSong"/>
    </div>
  </transition>
</template>

<script >
  import {mapActions} from 'vuex';
  import {playMode} from 'common/js/config';
  import Scroll from 'base/scroll/scroll';
  import Confirm from 'base/confirm/confirm';
  import AddSong from 'components/add-song/add-song';
  import {playerMixin} from 'common/js/mixin';

  export default {
    mixins: [playerMixin],
    data() {
      return {
        showFlag: false,
        refreshDelay: 120
      };
    },
    computed: {
      modeText() {
        return this.mode === playMode.sequence ? '顺序播放' : this.mode === playMode.random ? '随机播放' : '单曲循环';
      }
    },
    methods: {
      /**
       * 显示播放列表
       */
      show() {
        this.showFlag = true;
        setTimeout(() => {
          this.$refs.listContent.refresh();
          this.scrollToCurrent(this.currentSong);
        }, 20);
      },
      /**
       * 隐藏播放列表
       */
      hide() {
        this.showFlag = false;
      },
      /**
       * 正在播放的歌曲赋予icon-play的class
       * @param item 歌曲
       * @returns {string}
       */
      getCurrentIcon(item) {
        if (this.currentSong.id === item.id) {
          return 'icon-play';
        }
        return '';
      },
      /**
       * 选择歌曲
       * @param item 歌曲
       * @param index 歌曲序号
       */
      selectItem(item, index) {
        if (this.mode === playMode.random) {
          index = this.playlist.findIndex((song) => {
            return song.id === item.id;
          });
        }
        this.setCurrentIndex(index);
        this.setPlayingState(true);
      },
      /**
       * 点击歌曲，列表滚动到相应歌曲
       * @param current 点击的歌曲
       */
      scrollToCurrent(current) {
        const index = this.sequenceList.findIndex((song) => {
          return current.id === song.id;
        });
        this.$refs.listContent.scrollToElement(this.$refs.listItem[index], 300);
      },
      /**
       * 删除一首歌曲
       * @param item 歌曲
       */
      deleteOne(item) {
        this.deleteSong(item);
        console.log(item);
        if (!this.playlist.length) {
          this.hide();
        }
      },
      /**
       * 显示确认弹框
       */
      showConfirm() {
        this.$refs.confirm.show();
      },
      /**
       * 确认清空全部歌曲列表
       */
      confirmClear() {
        this.deleteSongList();
        this.hide();
      },
      /**
       * 添加歌曲
       */
      addSong() {
        this.$refs.addSong.show();
      },
      ...mapActions([
        'deleteSong',
        'deleteSongList'
      ])
    },
    watch: {
      currentSong(newSong, oldSong) {
        if (!this.showFlag || newSong.id === oldSong.id) {
          return;
        }
        setTimeout(() => {
          this.scrollToCurrent(newSong);
        }, 20);
      }
    },
    components: {
      Scroll,
      Confirm,
      AddSong
    }
  };
</script>

